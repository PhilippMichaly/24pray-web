/**
 * Parallele Kommunikation der Kette: Buttons zu den vom Organisator hinterlegten
 * Gruppen (nur validierte Dienst-URLs). Absprung zum Dienst erst beim Klick.
 */
export function GroupLinksBar({ project }: { project: ProjectWithStats }) {
  const active = SERVICES.filter((s) => project[s.key]);
  if (active.length === 0) return null;

  return (
    <div className="flex flex-wrap items-center gap-2">
      <span className="text-sm text-ink-muted">{t('groupLinksLabel')}</span>
      {active.map((s) => (
        <a
          key={s.key}
          href={project[s.key]!}
          target="_blank"
          rel="noopener noreferrer"
          className="inline-flex items-center gap-1.5 rounded-md border bg-surface px-3 py-1.5 text-sm font-medium text-ink transition-colors hover:bg-surface-sunken focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-focus"
        >
          <svg viewBox="0 0 24 24" width="16" height="16" fill={s.color} aria-hidden>
            <path d={s.path} />
          </svg>
          {s.name}
        </a>
      ))}
    </div>
  );
}
