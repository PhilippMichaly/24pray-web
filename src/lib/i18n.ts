type TranslationKey = keyof typeof de;
type Locale = 'de' | 'en' | 'es' | 'he' | 'ar';

const RTL_LOCALES: ReadonlySet<Locale> = new Set(['he', 'ar']);
/** RTL-Locale? — von locale-context.tsx genutzt, um `dir` auf `<html>` zu setzen. */
export function isRtl(l: Locale): boolean {
  return RTL_LOCALES.has(l);
}

const catalogs: Record<Locale, Partial<Record<TranslationKey, string>>> = { de, en, es, he, ar };

// Umschaltbar (2026-07-08): Server rendert de, der LocaleProvider wendet nach dem
// Mount die Browser-Sprache bzw. die gespeicherte Wahl an (kein Geo-IP — bewusst).
let currentLocale: Locale = 'de';

export type { Locale };
export function getLocale(): Locale {
  return currentLocale;
}
export function setLocale(l: Locale): void {
  currentLocale = l;
}
/** BCP-47 für Intl.DateTimeFormat & Co. Arabisch bewusst ohne Regions-Suffix (`ar`) —
 * `Intl` wählt dann lokale (native) Ziffern/Kalenderkonventionen; im Bericht erwähnt. */
const INTL_LOCALE_MAP: Record<Locale, string> = {
  de: 'de-DE',
  en: 'en-GB',
  es: 'es-ES',
  he: 'he-IL',
  ar: 'ar',
};
export function intlLocale(): string {
  return INTL_LOCALE_MAP[currentLocale];
}
/** Native Sprachnamen — bewusst NICHT übersetzt (jeder erkennt seine Sprache im Original). */
export const LOCALE_NAMES: Record<Locale, string> = {
  de: 'Deutsch',
  en: 'English',
  es: 'Español',
  he: 'עברית',
  ar: 'العربية',
};
const LOCALE_KEY = '24pray:locale';
export const SUPPORTED_LOCALES: readonly Locale[] = ['de', 'en', 'es', 'he', 'ar'];
/** Manuelle Wahl (localStorage) schlägt Browser-Sprache; unbekannte Sprachen → Englisch. */
export function detectLocale(): Locale {
  try {
    const saved = localStorage.getItem(LOCALE_KEY);
    if ((SUPPORTED_LOCALES as string[]).includes(saved ?? '')) return saved as Locale;
  } catch {
    /* localStorage kann blockiert sein */
  }
  const lang = (typeof navigator !== 'undefined' ? navigator.language : 'de').toLowerCase();
  const match = SUPPORTED_LOCALES.find((l) => l !== 'en' && lang.startsWith(l));
  return match ?? 'en';
}
export function persistLocale(l: Locale): void {
  try {
    localStorage.setItem(LOCALE_KEY, l);
  } catch {
    /* s.o. */
  }
}

export function t(key: TranslationKey, params?: Record<string, string | number>): string {
  const raw = catalogs[currentLocale][key] ?? de[key];
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}

/**
 * Zentrale Stunden/Tage-Textwahl (Tages-Modus, slotDurationMinutes=1440): Komponenten
 * rufen `tUnit(isDayMode, hourKey, dayKey, params)` statt eigener if-Kaskaden.
 */
export function tUnit(
  isDayMode: boolean,
  hourKey: TranslationKey,
  dayKey: TranslationKey,
  params?: Record<string, string | number>,
): string {
  return t(isDayMode ? dayKey : hourKey, params);
}
